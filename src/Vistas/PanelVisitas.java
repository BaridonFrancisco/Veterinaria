    private void CargarMascotas(int idc) {
        ArrayList<Mascota> listaTratamientos = dataMascota.buscarMascotaPIdCliente(idc);
        for (Mascota t : listaTratamientos) {
            modelo.addRow(new Object[]{
                t.getIdMascota(),
                t.getAlias(),
                t.getSexo(),
                t.getEspecie(),
                t.getRaza()
            });

        }

    }
