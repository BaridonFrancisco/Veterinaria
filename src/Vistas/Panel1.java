                jNombre.setText(cli.getNombre());
                jDomicilio.setText(cli.getDireccion());
                jContactoAlternativo.setText(cli.getNombreAl());
                jTelefonoAlternativo.setText("" + cli.getTelefonoAl());
                jTelefono.setText("" + cli.getTelefono());
